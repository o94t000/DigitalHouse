// perro, gato, ventana, mesa
let traducir = "oso"

switch(traducir.toLowerCase){
    case "perro":
        console.log("dog")
    break;
    case "gato":
        console.log("cat")
    break;
    case "ventana":
        console.log("window")
    break;
    case "mesa":
        console.log("table")
    break;
    default:
        console.log("La palabra ingresada es incorrecta")
}